package lib

func Sumar(num1 float64, num2 float64) float64 {
	return num1 + num2
}
func Restar(num1 float64, num2 float64) float64 {
	return num1 - num2
}
func Multiplicar(num1 float64, num2 float64) float64 {
	return num1 * num2
}
func Dividir(num1 float64, num2 float64) float64 {
	return num1 / num2
}